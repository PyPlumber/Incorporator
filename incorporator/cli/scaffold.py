def files_for(scaffold_type: str, *, with_inflow: bool = False) -> Dict[str, str]:
    """Return a mapping of {filename: contents} for the requested scaffold.

    ``with_inflow`` adds an ``inflow.py`` template alongside the other
    files.  Defaults to ``False`` for ``--type stream`` (keep simple
    starts simple) and ``True`` for ``--type fjord`` (fjord pipelines
    almost always have user reducers).
    """
    if scaffold_type == "stream":
        files = {"pipeline.json": STREAM_TEMPLATE_JSON}
        if with_inflow:
            files["inflow.py"] = INFLOW_TEMPLATE_PY
        return files
    if scaffold_type == "fjord":
        files = {"pipeline.json": FJORD_TEMPLATE_JSON, "outflow.py": FJORD_TEMPLATE_PY}
        if with_inflow:
            files["inflow.py"] = INFLOW_TEMPLATE_PY
        return files
    raise ValueError(f"Unknown scaffold type: {scaffold_type!r}. Use 'stream' or 'fjord'.")


def write_scaffold(scaffold_type: str, output_dir: Path, *, with_inflow: bool = False) -> list[Path]:
    """Write the scaffold files into ``output_dir``.

    Returns the list of paths written. Raises FileExistsError if any target
    file already exists — never overwrites silently.
    """
    output_dir.mkdir(parents=True, exist_ok=True)
    files = files_for(scaffold_type, with_inflow=with_inflow)
    paths = [output_dir / name for name in files]
    existing = [p for p in paths if p.exists()]
    if existing:
        names = ", ".join(p.name for p in existing)
        raise FileExistsError(
            f"Refusing to overwrite existing file(s) in {output_dir}: {names}. "
            "Move them aside or choose a different --output-dir."
        )
    for name, content in files.items():
        (output_dir / name).write_text(content, encoding="utf-8")
    return paths
