"""Starter-file templates for ``incorporator init``.

Two scaffolds:

- ``stream`` → ``pipeline.json`` (single source ETL).
- ``fjord`` → ``pipeline.json`` + ``outflow.py`` (multi-source ETL with
  a user-defined ``outflow(state)`` join function).

Templates are hardcoded triple-quoted strings — no Jinja, no extra deps.
The CLI's ``init`` command writes them to disk and refuses to overwrite
existing files.

The JSON templates include ``//`` line comments above each key explaining
what to fill in. Because strict JSON disallows comments, the actual files
written use a leading ``_comment_`` key trick — readers ignore unknown
keys. Where developers add their own comments, they can either keep the
``_comment_`` keys or strip them; the validator ignores both.
"""

